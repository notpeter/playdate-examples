gfx = playdate.graphics

local s,ms = playdate.getSecondsSinceEpoch()
math.randomseed(ms,s)

local perlin_x = math.random() * 100
local perlin_y = math.random() * 100
local perlin_z = math.random() * 100    
local scale = 6

function lerp(minVal, maxVal, f)
	-- Linear interpolate.  perlin_lerp(0, 255, 0.5) == 127.5
	return minVal + f * (maxVal - minVal)
end

local x,y

	    gfx.setColor(gfx.kColorWhite)
		gfx.fillRect(0, 0, 400, 240)

function playdate.update()

	    gfx.setColor(gfx.kColorWhite)
		gfx.fillRect(0, 0, 400, 240)

    gfx.setColor(gfx.kColorBlack)

    local py = perlin_y

    for y = 0, 239, 8 do
    
	    local px = perlin_x

	    py += 0.1
		
	    for x = 0, 399, 8 do
	        px += 0.1
	        local p = gfx.perlin(px / scale, py / scale, perlin_z / scale, 0, 6, 0.4)

	        --print(p, #patterns)

	        gfx.setPattern(patterns[math.floor((p + 1) * #patterns / 2)])
	        gfx.fillRect(x, y, 8, 8)
	    end
	    
    end

	    perlin_x += 0.1
	    perlin_y += 0.1
	    perlin_z += 0.00001
    
end
